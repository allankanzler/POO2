    private void limparCampos(){
        tfPalavra.setText("");
        tfDica.setText("");
        tfPalavra.requestFocus();
        System.out.println(lista);
        
    }
    private void limparTudo(){
        limparCampos();
        lista.clear();
        taLista.setText("");
        btSalvar.setEnabled(false);
    }
