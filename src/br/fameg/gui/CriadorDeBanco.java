    private void btAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAdicionarActionPerformed
        // TODO add your handling code here:
        taLista.append(tfPalavra.getText()+"\n");
        Palavra p = new Palavra(tfDica.getText().toUpperCase(), tfPalavra.getText().toUpperCase().trim());
        lista.add(p);
        limparCampos();
        btSalvar.setEnabled(true);
    }//GEN-LAST:event_btAdicionarActionPerformed

    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
        // TODO add your handling code here:
        String arq = JOptionPane.showInputDialog("Escreva o nome do banco");
        File file = new File(arq+".txt");
        SalvadorDeBancoDePalavras.gravarBanco(lista, file);
        limparTudo();
               
    }//GEN-LAST:event_btSalvarActionPerformed

