    private void makeCard(String lineToParse) {
        String[] result = lineToParse.split("/");
        Palavra card = new Palavra(result[0], result[1]);
        listaDePalavras.add(card);
        textArea.append((listaDePalavras.size() + ". ") + "" + card.getClue()+" ("+card.getResposta().length()+")" + "\n");

    }
    private void limparCampos() {
        listaDePalavras.clear();
        textArea.setText("");
    }

