    private void btAbrirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAbrirActionPerformed
        // TODO add your handling code here:
        limparCampos();
        int returnVal = chooserAbrir.showOpenDialog(this);
        if (returnVal == JFileChooser.APPROVE_OPTION) {
            File file = chooserAbrir.getSelectedFile();
            try {
                BufferedReader reader = new BufferedReader(new FileReader(file));
                String line = "";
                while ((line = reader.readLine()) != null) {
                    makeCard(line);
                }
                System.out.println(listaDePalavras);
                reader.close();           
            } catch (IOException ex) {
                System.out.println("problem accessing file" + file.getAbsolutePath());
            }
        } else {
            System.out.println("File access cancelled by user.");
        }
        ArrayList<char[]> listaChar = TransformadorDePalavraEmVetor.transformar(listaDePalavras);
        List<char[]> cruzadinha = LogicaCruzadinha.fazerCruzadinha(listaChar);
        ad.setData(cruzadinha);
        
        
    }//GEN-LAST:event_btAbrirActionPerformed

